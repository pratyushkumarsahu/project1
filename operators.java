public class operators {
    public static void main(String[] args) {
        int a = 6 ;
       // int b= 4+a;
        a+= 3;
        System.out.println(a);
        System.out.println(6==6);//comparison operator
        System.out.println(64>5 && 65>7);// logical operator 
        System.out.println(64>5 || 65>7);//         ''
        System.out.println(2&3);//bit wise operator 
    }
}

