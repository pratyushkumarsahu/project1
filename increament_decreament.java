public class increament_decreament {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x++);//first b is assigned then i is increament 
        System.out.println(x);
        System.out.println(++x);//first b is incrreament then the value of i is shown
        System.out.println(x);
        // same goes for decreament operator.
    }
}
